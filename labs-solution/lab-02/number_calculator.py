# Step 2: Input Some Numbers
# This program demonstrates number input, conversion, and calculations

# Get two numbers from the user
first_number = int(input('Please enter a number: '))
second_number = int(input('Please enter another number: '))

# Calculate the sum
result = first_number + second_number

# Display the result and its type
print(f'The result of {first_number} + {second_number} is {result}')
print(f'The type of the value is {type(result)}')
