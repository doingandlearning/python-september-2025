my_variable = "This is a str"
print(my_variable)
print(type(my_variable))

my_variable = 314
print(my_variable)
print(type(my_variable))  # int -> integer -> a whole number

my_variable = 3.14
print(my_variable)
print(type(my_variable))  # float -> floating point number -> decimal

# Setting my variable to be True or False
my_variable = False
print(my_variable)    
print(type(my_variable)) # bool -> boolean -> true/false

my_variable = None
print(my_variable)
print(type(my_variable))  # NoneType -> null/undefined